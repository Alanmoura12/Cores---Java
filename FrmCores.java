    private void btnverificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnverificarActionPerformed
        //Botão verificar
       String cor;
       
       cor = txtcor.getText().toUpperCase();
       
       
        switch (cor) {
            case "AMARELO":
                JOptionPane.showMessageDialog(null, "Você digitou AMARELO");
                
            break;
                
            case "AZUL":
                JOptionPane.showMessageDialog(null, "Você digitou AZUL");
            break;
            
            case "VERMELHO":
                JOptionPane.showMessageDialog(null,"Você digitou VERMELHO");
            break;
            
            case "VERDE":
                JOptionPane.showMessageDialog(null,"Você digitou VERDE");
            break;
            
                     
            default:
                JOptionPane.showMessageDialog(null,"Desculpe não sei qual foi a COR digitada");
        }
        
        
        
        
    }//GEN-LAST:event_btnverificarActionPerformed

